    <h2>e.	How to get game items?</h2>
    <p>You can get game item by picking it up you find it or by joining a quest, there you’ll probably get game items as reward. You can collect game items up to 9 items, you can view it in your “Items” by clicking Items icon on the bottom right of the screen.</p>

    <h2>f.	What is quest and how does it work?</h2>
    <p>Quest is some kind of a task that you can do. It’s assigned in NPC. It’s a simple task like finding some game items. By finishing quest, you can get game items and other rewards.</p>
    
    <h2>g.	How to join a quest?</h2>
    <p>Find NPC and click on it, there will be dialog window appears. If there’s quest assigned in that NPC, there will be an option for you to do quest. Just follow the instruction and do the quest.</p>

		<h2>h.	How do energy and coins work?</h2>
		<p>Energy is measured by percentage. It could be required to do quests. When you have finished a quest, your energy will be decreased, depends on quest’s requirement. Your energy will be recharged automatically in a certain period of time. You can alse get energy points as a reward when you have finished certain quest.</p>
		<p>Coins indicate how rich you are in game. Coins is useful to get certain game items, or to join certain quests. Coins is pretty much the same thing with energy. It can be either requirements or a rewards from a quest. The difference is, coins can’t be automatically recharged like energy.</p>


  
