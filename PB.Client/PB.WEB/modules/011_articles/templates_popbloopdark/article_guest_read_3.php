  	<h1>HOW TO PLAY</h1>
    
    
    <h2>a.	What is avatar?</h2>
    <p>Avatar is a 3D representation of yourself in Popbloop. You can customize your avatar to be what you like. </p>
    
    <h2>b.	How to customize avatar?</h2>
    <p>Go to Avatar Editor page through navigation menu on popbloop.com. There you can browse avatar items on the right of your screen and preview your avatar’s preference on the left of your screen.</p>
		<p>There are two types of avatar items that you can customize. Features and style.</p>
		<p>By features, it means physical preferences and appearances of your avatar. It include gender, body type, skin color, hair, and face items (eyes, brows, and lips). These items are unisex and it all can be applied to whatever your avatar’s gender are. Features items will always be free items.</p>
		<p>By style, it means clothing and accessories. It include top wear, bottom wear, foot wear, head wear and accessories like watch or glasses. As a start, you will have some basic style for free, and then you can get hundreds of avatar styles in store.</p>
    
    <h2>c.	How to chat?</h2>
    <p>Actually there are two different way to communicate in Popbloop, shout and chat.</p>
    <p>By shout, it means chatting system in the game, you can accesss it via shout interface on the bottom left of you screen. You can simply put your cursor there (or just press tab) to post a shout. Then it will be automatically appears on your shout timeline on your profile page.</p>
    <p>By chat, it means instant messaging. This feature appears on web. You can access it via chat bar on the bottom right of the web.</p>

    <h2>d.	How to control avatar?</h2>
