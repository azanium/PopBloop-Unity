    <h2>a.	How to edit my profile?</h2>
    <p>Each users have profile page. You can add and edit some information about yourself and let other users know about you. Here you can show your bio, your friends, your shout timeline and also your achievement.</p>
    
    <h2>b.	How to add a friend?</h2>
    <p>You can add other user as your friend by clicking Add Friend button on his/her profile page. You can manage friends in Friends page. There, you can create boxes and put your friend into the box that you want. By box, it means list of friends that is categorized by keyword. For example: Family box, Work Colleague Box etc. </p>
    
    <h2>c.	How does shout work?</h2>
    <p>Shout is your speech. There’s two way to shout. First, you can shout from in game via shout interface. Your shout will appear as speech buble in 3D world. Then, your shout will also automatically appear on your shout timeline on web. Second, you can post your shout on your home page, from the shout composer. It will appear on your timeline as well. Your shout will also appear on your friends home page as friend’s shout.</p>
    
    <p>Shout is basically public. If you want to shout to a specific person, you can use direct shout. There you can choose which user do want to receive your shout. Then your shout will appear in your recipient’s inbox. </p>
    
    <h2>d.	How does chat work?</h2>
    <p>Chat is an instant messaging feature. You can only use chat on web. You can chat only to your friends. List of friend that available to chat to is appear on the bottom right of your screen. Simply click their name to start chat.</p>

    
  
